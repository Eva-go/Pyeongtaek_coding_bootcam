package Day2;

public class ForExam {
    public static void main(String[] args) {

        int m = 0;
        for (int j =2; j<=2;j++)
        {
            for(int i=1;i<=9;i++){
                m = j*i;
                System.out.printf("%d * %d = %d \n",j,i,m);
            }
        }


    }
}
